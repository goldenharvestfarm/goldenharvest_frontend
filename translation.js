// Get current language from localStorage or default to 'en'
function getCurrentLanguage() {
    return localStorage.getItem('ghf_language') || 'en';
}

// Set current language
function setCurrentLanguage(lang) {
    localStorage.setItem('ghf_language', lang);
}

// Translate page
function translatePage(lang) {
    const elements = document.querySelectorAll('[data-translate]');
    
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        
        if (translations[lang] && translations[lang][key]) {
            // Handle different element types
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                // Don't translate if element has a value (user input)
                if (!element.value) {
                    element.placeholder = translations[lang][key];
                }
            } else if (element.tagName === 'OPTION') {
                element.textContent = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });
    
    // Handle placeholder translations
    const placeholderElements = document.querySelectorAll('[data-translate-placeholder]');
    placeholderElements.forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        if (translations[lang] && translations[lang][key]) {
            element.placeholder = translations[lang][key];
        }
    });
    
    // Update page title
    document.title = translations[lang].meta_title;
    
    // Update language switchers
    const languageSwitchers = document.querySelectorAll('#languageSwitcher, #mobileLanguageSwitcher');
    languageSwitchers.forEach(switcher => {
        switcher.value = lang;
    });
}

// Change language function
function changeLanguage() {
    const desktopSwitcher = document.getElementById('languageSwitcher');
    const mobileSwitcher = document.getElementById('mobileLanguageSwitcher');
    
    // Get the language from whichever switcher was changed
    let newLang = desktopSwitcher.value;
    if (document.activeElement === mobileSwitcher) {
        newLang = mobileSwitcher.value;
    }
    
    // Update both switchers
    desktopSwitcher.value = newLang;
    mobileSwitcher.value = newLang;
    
    // Save and translate
    setCurrentLanguage(newLang);
    translatePage(newLang);
}

// Initialize translation on page load
document.addEventListener('DOMContentLoaded', () => {
    const currentLang = getCurrentLanguage();
    translatePage(currentLang);
});

// Export for use in other scripts
if (typeof module !== 'undefined' && module.exports) {
    module.exports = { translations, translatePage, changeLanguage, getCurrentLanguage };
}